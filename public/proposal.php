      <h1>Meetings</h1>
      <h2>Format</h2>
      <p>Proposal: Second Sunday of every month from 1-5 PM CT</p>
      <ul>
        <li>1-2 PM CT: Hangout, unstructured, free table</li>
        <li>2-4 PM CT: Official meeting business with: A topic, presentations, free discussion</li>
        <li>4-5 PM CT: Hangout, unstructured, free table, takedown</li>
      </ul>
      <p>A “free table” exists for people to place items on to offer them up for free to others in the club! You are required to take whatever you put on the free table back with you if no one takes it. Free table is first-come-first-serve.</p>
      <p>A topic will be announced at least 1 week in advance. Presentations and discussions can be “reserved” ahead of time or can be brought in without notice. Reserved spots go first with any remaining time given on a first-come-first-serve basis.</p>
      <h2>Location</h2>
      <p>Potential location types...</p>
      <ul>
        <li>Cafe</li>
        <li>Library</li>
        <li>Church</li>
        <li>Community center</li>
      </ul>
      <p>Requirements...</p>
      <ul>
        <li>Seating for everyone</li>
        <li>Sturdy and big tables to be able to accommodate CRTs, big towers, etc.</li>
        <li>Power outlets</li>
        <li>Parking lot</li>
        <li>Easy enough ability to get things from the parking lot into the meeting space</li>
      </ul>

