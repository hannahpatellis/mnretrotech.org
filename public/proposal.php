      <h1>Mission</h1>
      <p>This club will exist as a user’s group for retro/vintage computer/technology enthusiasts. Our goal is to…</p>
      <ul>
        <li>Connect people within the community,</li>
        <li>Preserve computing history,</li>
        <li>Provide educational outreach to the community,</li>
        <li>And share knowledge
      </ul>

      <h1>Guidelines</h1>
      <ul>
        <li>In-person meetings will be held within the metro area of the Twin Cities of Minnesota</li>
        <li>All ages welcome. Anyone under the age of 15 needs to be accompanied by a guardian, unless otherwise discussed.</li>
        <li>All tech is welcome! While the focus is retro/vintage, we welcome any discussion on anything unique/cool/exciting!</li>
        <li>A genuine interest in retro/vintage tech is all you need. Nothing else required – you don’t need a collection or to bring a project – come as you are.</li>
        <li>Leave all politics at the door.</li>
        <li>No sexual content, topics, or discussions.</li>
        <li>This is a safe and inclusive environment. Use inclusive language and keep things appropriate.</li>
        <li>Be respectful. Treat everyone with kindness and respect. No harassment, hate speech, or personal attacks.</li>
      </ul>
      <h2>What makes something “retro”? What about “vintage”?</h2>
      <ul>
        <li>Retro is typically seen as “recent past”</li>
        <li>Vintage is “of old, recognized, and enduring interest, importance, or quality”</li>
        <li>Antiques are of a “former/earlier time or period”</li>
      </ul>
      <p>A general rule of thumb could be that if it’s supported by a manufacturer, it’s probably not any of the above. While the primary focus is retro/vintage technology and computers, anything tech-related will likely be enjoyed by everyone. Use your best judgement and if you’re unsure, just ask!</p>
      <h2>What makes something a “computer” or “tech”?</h2>
      <ul>
        <li>Who knows!</li>
        <li>Does it have a microchip?</li>
        <li>Devices are welcome! Mini computer, micro computer, semi-modern, cool keyboard, PalmPilot, whatever! Bring you hobbyist things too: Tech/cyberdecks like Beepys, mods to your Wii, if you like it we probably will too.</li>
      </ul>

